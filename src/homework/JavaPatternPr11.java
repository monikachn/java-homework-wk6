/**
 * Write a Java program to display the following pattern.
 * Sample Pattern :
 * J a v v a
 * J a a v v a a
 * J J aaaaa V V aaaaa
 * J J a a V a a
 */
public class JavaPatternPr11 {
    public static void main(String[] args) {
        String Pattern = "J a v v a\n" +
                         "J a a v v a a\n" +
                         "J J aaaaa V V aaaaa\n" +
                          "J J a a V a a"    ;
        System.out.println(Pattern);
    }


}
